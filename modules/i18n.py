def t(key: str, **kwargs) -> str:
    """Return the UI string for `key` in the active ui_language.

    Falls back to pt-BR if the key is missing in the active language.
    Supports format placeholders: t("welcome", name="Pedro") → "Welcome, Pedro"
    """
    try:
        import streamlit as st
        lang = st.session_state.get("ui_language", "pt-BR")
    except Exception:
        lang = "pt-BR"

    text = (
        TRANSLATIONS.get(lang, {}).get(key)
        or TRANSLATIONS.get("pt-BR", {}).get(key)
        or key
    )
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text
